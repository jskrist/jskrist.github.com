<p>The University of Illinois describes this class as:</p>
<p>Introduction to design-for-manufacturability methodologies and tools; quality management (Taguchi, productivity function deployment, statistical process control, etc.); materials selection (new and traditional materials); designing for primary manufacturing processes (cutting fundamentals, casting, forming, and shaping); designing with plastics (snap-fits, integral hinges, etc.); design for assembly; design for inspection and metrology (datums, geometric tolerancing, and inspection equipment); computer-integrated manufacturing.</p>
<p>This class starts off with a lot of redundant information from <a href="../Sp09/ME330.php">ME 330</a>, but it is very fast paced and energetic.  The Lecturer, Dr. Flachsbart, is fun nd entertaining, and seems to really care about his student's understanding of the material.  the lectures are powerpoint based, which I normally dislike, but he has a "gameshow" style of teaching that makes it work.  You MUST print out the slides before each class which means you will eat up most of your printing pages for just this class, but this class is definitely worth it.  It is the first and only practical skills class that I have taken from this department.</p>

